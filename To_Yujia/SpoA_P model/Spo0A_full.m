function dx=Spo0A_full(t,x,gr,va,vc,vs,kg,kt,nt,p0e)
vs=vs*22/1.5*4;
kb=500;ke=10;kube=200;k1=200;k2=800;k3=200;k4=800;k5=500;k6=200;
kc5=700;kc6=10;ki=100;kap=12;kadp=2;K=1.4;K0=0.14;kdeg=0.2+gr;pv=0.6;
%p0e=0.05;
ng=4;ksda=50;vsda=vs;
IIG=x(1);
KinA=x(2);
KinA_Sda=x(3);
KinA_p=x(4);
KinA_p_Spo0F=x(5);
KinC=x(6);
KinC_p=x(7);
KinC_p_Spo0F=x(8);
Sda=x(9);
Spo0A=x(10);
Spo0A_p=x(11);
Spo0A_p_Spo0E=x(12);
Spo0B=x(13);
Spo0B_p=x(14);
Spo0B_p_Spo0A=x(15);
Spo0E=x(16);
Spo0F=x(17);
Spo0F_KinA=x(18);
Spo0F_p=x(19);
Spo0F_p_Spo0B=x(20);
TapA=x(21);
dx(1)=0.2*(Spo0A_p^ng)/(kg^ng+Spo0A_p^ng)*fglobal(gr,0.43)*1-(kdeg+1)*IIG*1;
dx(2)=va*fglobal(gr,0.7)*1+k6*KinA_p_Spo0F*1-kb*KinA*Spo0F_p*1-kap*KinA*1+kadp*KinA_p*1-kb*Spo0F*KinA*1+ki*Spo0F_KinA*1-kb*KinA*Sda*1+5*KinA_Sda*1+9*KinA_Sda*1-kdeg*KinA*1;
dx(3)=kb*KinA*Sda*1-5*KinA_Sda*1-9*KinA_Sda*1-(kdeg)*KinA_Sda*1;
dx(4)=-kb*KinA_p*Spo0F*1+k5*KinA_p_Spo0F*1+kap*KinA*1-kadp*KinA_p*1-kdeg*KinA_p*1;
dx(5)=kb*KinA_p*Spo0F*1-k5*KinA_p_Spo0F*1-k6*KinA_p_Spo0F*1+kb*KinA*Spo0F_p*1-kdeg*KinA_p_Spo0F*1;
dx(6)=vc*fglobal(gr,0.7)*1+kc6*KinC_p_Spo0F*1-kb*KinC*Spo0F_p*1-kap*KinC*1+kadp*KinC_p*1-kdeg*KinC*1;
dx(7)=-kb*KinC_p*Spo0F*1+kc5*KinC_p_Spo0F*1+kap*KinC*1-kadp*KinC_p*1-kdeg*KinC_p*1;
dx(8)=kb*KinC_p*Spo0F*1-kc5*KinC_p_Spo0F*1-kc6*KinC_p_Spo0F*1+kb*KinC*Spo0F_p*1-kdeg*KinC_p_Spo0F*1;
dx(9)=vsda*fglobal(gr,0.7)*1-kb*KinA*Sda*1+5*KinA_Sda*1-(gr+9)*Sda*1;
dx(10)=(0.4+0.5*(Spo0A_p^2)/(Spo0A_p^2+0.05^2))*fglobal(gr,0.8)*1-kb*Spo0B_p*Spo0A*1+k1*Spo0B_p_Spo0A*1+ke*Spo0A_p_Spo0E*1-kdeg*Spo0A*1-kdeg*Spo0A*1;
dx(11)=k2*Spo0B_p_Spo0A*1-kb*Spo0B*Spo0A_p*1-kb*Spo0A_p*Spo0E*1+kube*Spo0A_p_Spo0E*1-kdeg*Spo0A_p*1-kdeg*Spo0A_p*1;
dx(12)=kb*Spo0A_p*Spo0E*1-kube*Spo0A_p_Spo0E*1-ke*Spo0A_p_Spo0E*1-kdeg*Spo0A_p_Spo0E*1;
dx(13)=2*fglobal(gr,0.7)*1+k2*Spo0B_p_Spo0A*1-kb*Spo0B*Spo0A_p*1-kb*Spo0F_p*Spo0B*1+k3*Spo0F_p_Spo0B*1-kdeg*Spo0B*1;
dx(14)=-kb*Spo0B_p*Spo0A*1+k1*Spo0B_p_Spo0A*1+k4*Spo0F_p_Spo0B*1-kb*Spo0F*Spo0B_p*1-kdeg*Spo0B_p*1;
dx(15)=kb*Spo0B_p*Spo0A*1-k1*Spo0B_p_Spo0A*1-k2*Spo0B_p_Spo0A*1+kb*Spo0B*Spo0A_p*1-kdeg*Spo0B_p_Spo0A*1;
dx(16)=p0e*fglobal(gr,0.7)*1-kb*Spo0A_p*Spo0E*1+kube*Spo0A_p_Spo0E*1+ke*Spo0A_p_Spo0E*1-kdeg*Spo0E*1;
dx(17)=2*fglobal(gr,0.2)*1+k4*Spo0F_p_Spo0B*1-kb*Spo0F*Spo0B_p*1-kb*KinA_p*Spo0F*1+k5*KinA_p_Spo0F*1-kb*KinC_p*Spo0F*1+kc5*KinC_p_Spo0F*1-kb*Spo0F*KinA*1+ki*Spo0F_KinA*1-kdeg*Spo0F*1;
dx(18)=kb*Spo0F*KinA*1-ki*Spo0F_KinA*1;
dx(19)=-kb*Spo0F_p*Spo0B*1+k3*Spo0F_p_Spo0B*1+k6*KinA_p_Spo0F*1-kb*KinA*Spo0F_p*1+kc6*KinC_p_Spo0F*1-kb*KinC*Spo0F_p*1-kdeg*Spo0F_p*1;
dx(20)=kb*Spo0F_p*Spo0B*1-k3*Spo0F_p_Spo0B*1-k4*Spo0F_p_Spo0B*1+kb*Spo0F*Spo0B_p*1-kdeg*Spo0F_p_Spo0B*1;
dx(21)=0.2*(Spo0A_p^nt)/(kt^nt+Spo0A_p^nt)*fglobal(gr,0.43)*1-(kdeg+1)*TapA*1;
dx=dx';
end
